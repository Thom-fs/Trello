     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // $name=  Storage::disk('local')->put('avatar',$request->avatar);

        // $request->file('avatar->store');

        $request->validate([
            'wallpaper' => 'required|mimes:jpg,png,jpeg'
        ]);
        $newImageName = $request->wallpaper->store('Images');
        $request->wallpaper->move(public_path('Images'), $newImageName);

        $post = [
            'wallpaper' => $newImageName
        ];


        image::create($post);

        return redirect()->back();
    }

